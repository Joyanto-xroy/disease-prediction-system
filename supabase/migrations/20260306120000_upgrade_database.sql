/*
  # Upgrade clinical support system database

  1. Update profiles table
    - Add verification_status (pending, verified, rejected)
    - Add license_url
    - Add doctor_id, clinic_permit_id for role-specific data

  2. New Tables
    - `patients`
      - Basic demographic data, medical history
    - `prescriptions`
      - Link to patients, doctor, medications
    - `reports`
      - Medical reports generated by doctors
    - `patient_visits`
      - Track patient visits and symptoms

  3. Security
    - Update RLS policies for new tables
*/

-- Update profiles table
ALTER TABLE profiles ADD COLUMN IF NOT EXISTS verification_status text DEFAULT 'pending' CHECK (verification_status IN ('pending', 'verified', 'rejected'));
ALTER TABLE profiles ADD COLUMN IF NOT EXISTS license_url text;
ALTER TABLE profiles ADD COLUMN IF NOT EXISTS doctor_id text;
ALTER TABLE profiles ADD COLUMN IF NOT EXISTS clinic_permit_id text;

-- Create patients table
CREATE TABLE IF NOT EXISTS patients (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  created_by uuid REFERENCES profiles(id) ON DELETE CASCADE,
  full_name text NOT NULL,
  date_of_birth date NOT NULL,
  gender text CHECK (gender IN ('male', 'female', 'other')),
  phone text,
  email text,
  address text,
  emergency_contact_name text,
  emergency_contact_phone text,
  medical_history text,
  allergies text,
  current_medications text,
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

-- Create patient_visits table
CREATE TABLE IF NOT EXISTS patient_visits (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  patient_id uuid REFERENCES patients(id) ON DELETE CASCADE,
  doctor_id uuid REFERENCES profiles(id) ON DELETE CASCADE,
  visit_date timestamptz DEFAULT now(),
  symptoms text,
  diagnosis text,
  notes text,
  test_results text,
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

-- Create prescriptions table
CREATE TABLE IF NOT EXISTS prescriptions (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  patient_id uuid REFERENCES patients(id) ON DELETE CASCADE,
  doctor_id uuid REFERENCES profiles(id) ON DELETE CASCADE,
  visit_id uuid REFERENCES patient_visits(id) ON DELETE CASCADE,
  medications jsonb NOT NULL, -- Array of medication objects
  instructions text,
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

-- Create reports table
CREATE TABLE IF NOT EXISTS reports (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  patient_id uuid REFERENCES patients(id) ON DELETE CASCADE,
  doctor_id uuid REFERENCES profiles(id) ON DELETE CASCADE,
  report_type text NOT NULL, -- 'consultation', 'lab', 'imaging', etc.
  title text NOT NULL,
  content text NOT NULL,
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

-- Enable RLS
ALTER TABLE patients ENABLE ROW LEVEL SECURITY;
ALTER TABLE patient_visits ENABLE ROW LEVEL SECURITY;
ALTER TABLE prescriptions ENABLE ROW LEVEL SECURITY;
ALTER TABLE reports ENABLE ROW LEVEL SECURITY;

-- RLS Policies for patients
CREATE POLICY "Doctors and admins can read all patients"
  ON patients FOR SELECT
  TO authenticated
  USING (
    EXISTS (
      SELECT 1 FROM profiles
      WHERE profiles.id = auth.uid()
      AND profiles.role IN ('doctor', 'admin')
      AND profiles.verification_status = 'verified'
    )
  );

CREATE POLICY "Doctors and admins can insert patients"
  ON patients FOR INSERT
  TO authenticated
  WITH CHECK (
    EXISTS (
      SELECT 1 FROM profiles
      WHERE profiles.id = auth.uid()
      AND profiles.role IN ('doctor', 'admin')
      AND profiles.verification_status = 'verified'
    )
  );

CREATE POLICY "Doctors and admins can update patients"
  ON patients FOR UPDATE
  TO authenticated
  USING (
    EXISTS (
      SELECT 1 FROM profiles
      WHERE profiles.id = auth.uid()
      AND profiles.role IN ('doctor', 'admin')
      AND profiles.verification_status = 'verified'
    )
  );

-- RLS Policies for patient_visits
